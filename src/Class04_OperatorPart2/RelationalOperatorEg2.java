package Class04_OperatorPart2;

// <, >, >=, <=, ==, !=

public class RelationalOperatorEg2 {
    static void main(String[] args) {

        int a = 50, b = 50;

        System.out.println(a < b); // f
        System.out.println(a > b); // f
        System.out.println(a <= b); // t
        System.out.println(a >= b); // t
        System.out.println(a == b);// t
        System.out.println(a != b); // f

    }
}
