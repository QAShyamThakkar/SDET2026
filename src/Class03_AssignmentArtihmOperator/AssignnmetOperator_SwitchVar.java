package Class03_AssignmentArtihmOperator;


//Assignment Operator

public class AssignnmetOperator_SwitchVar {


    static void main(String[] args) {

//        int c; //Declaration
//        c=10;  // Assign

        //Input
        int a = 100;
        int b = 200;
        int c;

        //Logic
        c=a;   //c=10, a=10
        a=b;   //a=20, b=20
        b=c;   //b=10, c=10

        //Output
        System.out.println(a); //20
        System.out.println(b); //10


    }

}
