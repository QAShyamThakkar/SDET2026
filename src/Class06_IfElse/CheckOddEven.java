package Class06_IfElse;

public class CheckOddEven {
    static void main(String[] args) {

        //Input
        int a = 2011;  // entered by user

        //Logic
        if (a%2 == 0) {
            //executed if the condition is true ;
            System.out.println( a + " is an even number");  // //104 is an even number
        } else {
            //executed if the condition is false;
            System.out.println( a + " is an odd number");  //103 is an odd number
        }


    }
}
