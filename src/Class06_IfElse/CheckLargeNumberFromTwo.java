package Class06_IfElse;

public class CheckLargeNumberFromTwo {
    static void main(String[] args) {

        // Input - Can be changed by user
        int a = 1000;
        int b = 500;


        //Output
        if (a > b) {
            System.out.println(a + " is the larger number");
        } else {
            System.out.println(b + " is the larger number");
        }

    }

}
