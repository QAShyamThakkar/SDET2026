package Class07_LadderIfElseandSwitch;

public class LadderIfElseEg1A {
    static void main(String[] args) {

        int num = -20;

        if (num > 0) {
            System.out.println(num + " is a Positive");
        } else if (num == 0) {
            System.out.println(num + " is a Zero");
        } else {
            System.out.println(num + " is a Negative");
        }


    }


}
