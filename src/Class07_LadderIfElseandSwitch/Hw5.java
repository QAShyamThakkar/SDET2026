package Class07_LadderIfElseandSwitch;

public class Hw5 {
    static void main(String[] args) {
        // 1-


        int num = 3;

        if (num == 1) {
            System.out.println("31 days");
        } else if (num == 2) {
            System.out.println("28/29 days");
        } else if (num == 3) {
            System.out.println("31 days");
        } else if (num == 4) {
            System.out.println("30 days");
        }
    }
}
