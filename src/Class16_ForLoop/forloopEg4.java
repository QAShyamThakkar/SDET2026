//Print 1 10 2 9 3 8 4 7 5 6

public class forloopEg4 {
    static void main(String[] args) {

        for (int i = 1, j = 10; i <= 5 && j >= 6; i++, j--) {

            System.out.println(i);
            System.out.println(j);

        }


    }

}

