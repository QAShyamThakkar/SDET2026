package Class02_Variables;

public class VariablesEg2 {
    static void main(String[] args) {
        int num1 = 100;
        int num2 = 50;
        int num3 = num1-num2;

        System.out.println(100+200);  //300
        System.out.println("100+200"); //100+200

        System.out.println(num1+num2); //300

        System.out.println(num1*num2); //300

        System.out.println(num3);


    }

}
