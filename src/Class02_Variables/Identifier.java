package Class02_Variables;

public class Identifier {
    static void main(String[] args) {

        int ______ = 10;


        //Identifier Rules:

//       1. Space is not allowed
//       2. 0-9, A-Z, _, $
//       3. Can not start with digit
//       4. Can not use Java Reserved Keywords
//       5. It is case-sensitive  Eg 'A' is not equal 'a'
//
//        1. num1 - Valid
//        2. num4 - Valid
//        3. num_4 - Valid
//        4. __23__ - Valid
//        5. 2ndNum - Invalid
//        6. $$$$   - Valid



    }
}
