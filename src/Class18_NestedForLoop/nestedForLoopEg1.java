package Class18_NestedForLoop;

public class nestedForLoopEg1 {
    static void main(String[] args) {

        //10 R --- 5 C
        //Print from 1 to 5

        for (int i = 1; i <= 5; i++) {

            for (int j = 1; j <= 4; j++) {
                System.out.print("Hello ");
            }

            System.out.println();
        }


    }


}
