package Class12_WhileLoop;

public class WhileLoopEg5 {
    static void main(String[] args) {
        //Print Hello 5 times in the output

        int i = 0;

        while (i <= 5) {
            System.out.println("Hello");  //6 times
            i = i + 1;  //3
        }

        System.out.println("End");
    }

}
